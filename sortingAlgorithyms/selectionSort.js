// *********************************************************************************************************
// BUBBLE SORT
// *********************************************************************************************************
function bubbleSort(arr) {
  const n = arr.length;

  for (let i = 0; i < n; i++) {
    for (let j = 0; j < n - 1 - i; j++) {
      if (arr[j] > arr[j + 1]) {
        [arr[j], arr[j + 1]] = [arr[j + 1], arr[j]];
      }
    }
  }

  console.log(arr);
}

bubbleSort([2, 5, 10, 3, 8, 5, 4, 6]);
