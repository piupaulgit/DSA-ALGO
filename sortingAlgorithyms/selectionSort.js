// *********************************************************************************************************
// SELECTION SORT
// *********************************************************************************************************
const selectionSort = (array) => {
  for (let i = 0; i < array.length; i++) {
    let small = i;
    for (let j = i + 1; j < array.length; j++) {
      if (array[small] > array[j]) {
        small = j;
      }
    }

    if (small !== i) {
      [array[small], array[i]] = [array[i], array[small]];
    }
  }

  console.log(array);
};

selectionSort([2, 5, 3, 8, 5, 4, 6]);
